-- ===================================================================
-- 修复项目考勤设置 - 诊断和修复脚本
-- ===================================================================

-- 第1步：查看当前所有项目的考勤设置
SELECT 
    id,
    name,
    code,
    require_attendance AS '新字段(0=无需考勤,1=需要考勤)',
    requires_attendance AS '旧字段',
    CASE 
        WHEN require_attendance = 1 THEN '✅ 需要考勤'
        WHEN require_attendance = 0 THEN '❌ 无需考勤'
        ELSE '⚠️ NULL或未设置'
    END AS '当前设置'
FROM projects
ORDER BY id;

-- ===================================================================

-- 第2步：将所有项目设置为"无需考勤"（用于测试）
-- 执行后，所有项目都应该可以直接生成工资表
UPDATE projects 
SET require_attendance = 0
WHERE 1=1;

-- ===================================================================

-- 第3步：验证更新结果
SELECT 
    id,
    name,
    require_attendance AS '考勤设置(应该都是0)',
    CASE 
        WHEN require_attendance = 0 THEN '✅ 已设置为无需考勤'
        ELSE '❌ 设置失败'
    END AS '验证结果'
FROM projects
ORDER BY id;

-- ===================================================================
-- 说明：
-- 1. 先执行第1步，查看当前设置
-- 2. 再执行第2步，将所有项目设置为"无需考勤"
-- 3. 最后执行第3步，验证设置是否成功
-- 4. 然后刷新浏览器，测试工资表生成功能
-- ===================================================================

